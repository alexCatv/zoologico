    private void animalesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_animalesActionPerformed
        // TODO add your handling code here:
        modelo.getDataVector().removeAllElements();
        
        String [] titulo={"Nombre","Especie","Subespecie","Peso"};
        modelo.setColumnIdentifiers(titulo);
        for (int i = 0; i < listaAnimal.size(); i++) {
            Animales e= listaAnimal.get(i);
            Object row[]={e.getName(),e.getEspecie(),e.getSubespecie(),e.getPeso()};
            modelo.addRow(row);
        }
    }//GEN-LAST:event_animalesActionPerformed

    private void cuidadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cuidadosActionPerformed
        // TODO add your handling code here:
        modelo.getDataVector().removeAllElements();
        String [] titulo={"Comida","Habitat","Coste","Cantidad","Periodicidad","Animal"};
        modelo.setColumnIdentifiers(titulo);
        for (int i = 0; i < listaCuidados.size(); i++) {
            Cuidados e= listaCuidados.get(i);
            Object row[]={e.getTipoComida(),e.getHabitat(),e.getCostePromedio(),e.getCantidadComidaKG(),e.getPeriodicidadComidaDias(),e.getAnimal().getName()};
            modelo.addRow(row);
        }
    }//GEN-LAST:event_cuidadosActionPerformed

